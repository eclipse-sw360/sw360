package org.eclipse.sw360.common.utils.converter.common;

import org.eclipse.sw360.datahandler.services.common.Comment;

public final class CommentConverter {

    private CommentConverter() {}

    public static Comment fromThrift(org.eclipse.sw360.datahandler.thrift.Comment thrift) {
        if (thrift == null) {
            return null;
        }
        Comment pojo = new Comment();
        if (thrift.isSetAutoGenerated()) {
            pojo.setAutoGenerated(thrift.isAutoGenerated());
        }
        if (thrift.isSetCommentedBy()) {
            pojo.setCommentedBy(thrift.getCommentedBy());
        }
        if (thrift.isSetCommentedOn()) {
            pojo.setCommentedOn(thrift.getCommentedOn());
        }
        if (thrift.isSetDateTime()) {
            pojo.setDateTime(thrift.getDateTime());
        }
        if (thrift.isSetText()) {
            pojo.setText(thrift.getText());
        }
        if (thrift.isSetUsername()) {
            pojo.setUsername(thrift.getUsername());
        }
        return pojo;
    }

    public static org.eclipse.sw360.datahandler.thrift.Comment toThrift(Comment pojo) {
        if (pojo == null) {
            return null;
        }
        org.eclipse.sw360.datahandler.thrift.Comment thrift = new org.eclipse.sw360.datahandler.thrift.Comment();
        if (pojo.getAutoGenerated() != null) {
            thrift.setAutoGenerated(pojo.getAutoGenerated());
        }
        if (pojo.getCommentedBy() != null) {
            thrift.setCommentedBy(pojo.getCommentedBy());
        }
        if (pojo.getCommentedOn() != null) {
            thrift.setCommentedOn(pojo.getCommentedOn());
        }
        if (pojo.getDateTime() != null) {
            thrift.setDateTime(pojo.getDateTime());
        }
        if (pojo.getText() != null) {
            thrift.setText(pojo.getText());
        }
        if (pojo.getUsername() != null) {
            thrift.setUsername(pojo.getUsername());
        }
        return thrift;
    }
}
