package org.eclipse.sw360.rest.resourceserver.architecture;

import com.tngtech.archunit.core.domain.JavaClass;
import com.tngtech.archunit.lang.ArchCondition;
import com.tngtech.archunit.lang.ArchRule;
import com.tngtech.archunit.lang.ConditionEvents;
import com.tngtech.archunit.lang.SimpleConditionEvent;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;

import java.util.Set;

import static com.tngtech.archunit.lang.syntax.ArchRuleDefinition.classes;
import static com.tngtech.archunit.lang.syntax.ArchRuleDefinition.noClasses;

/**
 * Validates third-party dependency governance in the SW360 REST module.
 * <p>
 * These rules enforce an <strong>allowlist approach</strong> for external dependencies.
 * Only approved libraries may be used in the REST module. This prevents:
 * <ul>
 *   <li>Unreviewed third-party libraries being added without team awareness</li>
 *   <li>Duplicate libraries solving the same problem (e.g., multiple JSON libs)</li>
 *   <li>License-incompatible dependencies (SW360 is EPL-2.0)</li>
 * </ul>
 * <p>
 * <strong>Adding a new dependency:</strong> If a new library is genuinely needed,
 * add its root package to {@code ALLOWED_PACKAGE_PREFIXES} and document the rationale.
 */
@DisplayName("Dependency Governance Rules")
class DependencyGovernanceRulesTest extends SW360ArchitectureTest {

    /**
     * Approved third-party library root packages for the REST module.
     * <p>
     * Each entry represents a library that has been reviewed and approved.
     * To add a new dependency, add its package prefix here with a justification.
     */
    private static final Set<String> ALLOWED_PACKAGE_PREFIXES = Set.of(
            // Java Platform / Jakarta EE
            "java.",
            "jakarta.",
            // SW360 Project
            "org.eclipse.sw360.",
            // Spring Ecosystem
            "org.springframework.",
            // SpringDoc OpenAPI
            "org.springdoc.",
            // Apache Libraries
            "org.apache.thrift.",
            "org.apache.logging.",
            "org.apache.commons.",
            // Logging
            "org.slf4j.",
            // JSON / Serialization
            "com.fasterxml.jackson.",
            "tools.jackson.",
            "com.google.common.",
            "com.google.gson.",
            // OpenAPI / Documentation
            "io.swagger.",
            // Lombok (source annotations and generated bytecode)
            "lombok.",
            // Annotations
            "org.jetbrains.",
            // JWT libraries
            "org.jose4j."
    );

    @Test
    @DisplayName("REST module should only depend on approved third-party libraries")
    void restModuleShouldOnlyUseApprovedLibraries() {
        ArchCondition<JavaClass> onlyDependOnApprovedLibraries =
                new ArchCondition<>("only depend on approved third-party libraries") {
                    @Override
                    public void check(JavaClass javaClass, ConditionEvents events) {
                        javaClass.getDirectDependenciesFromSelf().stream()
                                .map(dep -> dep.getTargetClass())
                                .filter(target -> !target.getPackageName().isEmpty())
                                .filter(target -> !isInAllowedPackage(target.getPackageName()))
                                .forEach(target -> events.add(SimpleConditionEvent.violated(
                                        javaClass,
                                        String.format("%s depends on unapproved library: %s — " +
                                                        "add to ALLOWED_PACKAGE_PREFIXES in " +
                                                        "DependencyGovernanceRulesTest if intentional",
                                                javaClass.getSimpleName(),
                                                target.getFullName()))));
                    }

                    private boolean isInAllowedPackage(String packageName) {
                        // Handle array type names (e.g., "[Lorg.apache.thrift...")
                        String normalized = packageName.replaceFirst("^\\[+L?", "");
                        // Handle root-level packages like "lombok" (no subpackages)
                        return ALLOWED_PACKAGE_PREFIXES.stream()
                                .anyMatch(prefix -> normalized.startsWith(prefix)
                                        || prefix.startsWith(normalized + "."));
                    }
                };

        ArchRule rule = classes()
                .that().resideInAPackage("..rest.resourceserver..")
                .should(onlyDependOnApprovedLibraries)
                .as("REST module should only depend on approved third-party libraries — " +
                        "review and add new dependencies to the allowlist");

        rule.check(restClasses);
    }

    // ========== Banned Libraries ==========

    @Test
    @DisplayName("Classes should not use Apache HttpClient directly — use Spring RestTemplate or WebClient")
    void noClassShouldUseApacheHttpClient() {
        ArchRule rule = noClasses()
                .that().resideInAPackage("..rest.resourceserver..")
                .should().dependOnClassesThat()
                .resideInAPackage("org.apache.http..")
                .as("Do not use Apache HttpClient directly — " +
                        "use Spring's RestTemplate or WebClient for HTTP calls");

        rule.check(restClasses);
    }

    @Test
    @DisplayName("Classes should not use OkHttp directly — use Spring RestTemplate or WebClient")
    void noClassShouldUseOkHttp() {
        ArchRule rule = noClasses()
                .that().resideInAPackage("..rest.resourceserver..")
                .should().dependOnClassesThat()
                .resideInAPackage("okhttp3..")
                .as("Do not use OkHttp directly — " +
                        "use Spring's RestTemplate or WebClient for HTTP calls");

        rule.check(restClasses);
    }

    @Test
    @DisplayName("Classes should not use Gson directly — use Jackson (project standard)")
    void noClassShouldUseGsonDirectly() {
        // Gson is a transitive dependency from Cloudant SDK but should not be used
        // directly in REST module source code.
        // TODO: ProjectController.GSON and SW360ReportController should be refactored to use Jackson
        ArchRule rule = noClasses()
                .that().resideInAPackage("..rest.resourceserver..")
                .and().haveSimpleNameNotEndingWith("Builder")
                .and().doNotHaveSimpleName("ProjectController")
                .and().doNotHaveSimpleName("SW360ReportController")
                .should().dependOnClassesThat()
                .resideInAPackage("com.google.gson..")
                .as("Do not use Gson directly — " +
                        "Jackson is the project standard for JSON processing");

        rule.check(restClasses);
    }

    @Test
    @DisplayName("Classes should not use org.json directly — use Jackson (project standard)")
    void noClassShouldUseOrgJson() {
        ArchRule rule = noClasses()
                .that().resideInAPackage("..rest.resourceserver..")
                .should().dependOnClassesThat()
                .resideInAPackage("org.json..")
                .as("Do not use org.json directly — " +
                        "Jackson is the project standard for JSON processing");

        rule.check(restClasses);
    }

    @Test
    @DisplayName("Classes should not use JUnit 4 assertions — use JUnit 5 or AssertJ")
    void noClassShouldUseJUnit4Assertions() {
        ArchRule rule = noClasses()
                .that().resideInAPackage("..rest.resourceserver..")
                .should().dependOnClassesThat()
                .haveFullyQualifiedName("junit.framework.Assert")
                .as("Do not use JUnit 4 assertions — use JUnit 5 Assertions or AssertJ");

        rule.check(restClasses);
    }
}
